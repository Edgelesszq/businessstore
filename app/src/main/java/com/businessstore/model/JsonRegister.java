package com.businessstore.model;

public class JsonRegister {
    private String sellerId;
    private String sellerNum;
    private String numActiva;
    private String snumActiva;
    private String verifiCode;



    public String getSellerId() {
        return sellerId;
    }

    public void setSellerId(String sellerId) {
        this.sellerId = sellerId;
    }

    public String getSellerNum() {
        return sellerNum;
    }

    public void setSellerNum(String sellerNum) {
        this.sellerNum = sellerNum;
    }

    public String getNumActiva() {
        return numActiva;
    }

    public void setNumActiva(String numActiva) {
        this.numActiva = numActiva;
    }

    public String getSnumActiva() {
        return snumActiva;
    }

    public void setSnumActiva(String snumActiva) {
        this.snumActiva = snumActiva;
    }

    public String getVerifiCode() {
        return verifiCode;
    }

    public void setVerifiCode(String verifiCode) {
        this.verifiCode = verifiCode;
    }
}
